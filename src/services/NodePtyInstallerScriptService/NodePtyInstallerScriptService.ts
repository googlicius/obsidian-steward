  NODE_PTY_INSTALLER_LATEST_PS1_BASENAME,
  NODE_PTY_INSTALLER_TEMPLATE_VERSION,
  NODE_PTY_PREBUILT_OWNER,
  NODE_PTY_PREBUILT_PACKAGE_VERSION,
  NODE_PTY_PREBUILT_REPO,
  buildNodePtyInstallerVersionedBasename,
