/**
 * Bundles heavy npm deps into IIFEs, LZ-compresses each, and emits
 * `src/generated/bundled*Payload.ts` for runtime eval (see `src/utils/bundledLibs.ts`).
 *
 * Multiple entries: each has its own `globalName` on `globalThis` after eval.
 */
import esbuild from 'esbuild';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import LZString from 'lz-string';
import { sharedExternal } from '../esbuild.shared.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.join(__dirname, '..');
const scriptFileName = path.basename(__filename);

function getBundleConfig({ entryPoints, globalName, outFile, exportConst }) {
  return {
    entryPoints,
    bundle: true,
    platform: 'browser',
    format: 'iife',
    globalName,
    target: 'es2018',
    minify: true,
    write: false,
    external: sharedExternal,
    define: {
      'process.env.NODE_ENV': '"production"',
    },
    logLevel: 'info',
    exportConst,
    outFile,
  };
}

const bundles = [
  getBundleConfig({
    entryPoints: ['src/bundled-libs-entry.ts'],
    globalName: '__stewardBundledLibs',
    exportConst: 'BUNDLED_LIBS_LZ_B64',
    outFile: 'bundledLibsPayload.ts',
  }),
  getBundleConfig({
    entryPoints: ['src/bundled-libs-desktop-entry.ts'],
    globalName: '__stewardBundledDesktopLibs',
    exportConst: 'BUNDLED_DESKTOP_LIBS_LZ_B64',
    outFile: 'bundledDesktopLibsPayload.ts',
  }),
];

const outDir = path.join(root, 'src/generated');
fs.mkdirSync(outDir, { recursive: true });

for (const bundle of bundles) {
  const { exportConst, outFile, ...esbuildOptions } = bundle;
  const result = await esbuild.build(esbuildOptions);
  const js = result.outputFiles[0].text;
  const b64 = LZString.compressToBase64(js);
  const outPath = path.join(outDir, outFile);
  fs.writeFileSync(
    outPath,
    `// AUTO-GENERATED by ${scriptFileName} — do not edit manually\n` +
      `export const ${exportConst} = ${JSON.stringify(b64)};\n`,
    'utf8'
  );
  console.log(
    `Wrote ${path.relative(root, outPath)} (base64 length ${b64.length}, minified JS ${js.length} bytes)`
  );
}
