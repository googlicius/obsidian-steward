/**
 * Bundles heavy npm dependencies (ai + @ai-sdk/* + ollama + node-pty JS + @xterm/* + socket.io +
 * socket.io-client + strip-ansi + mustache) into one IIFE,
 * LZ-compresses it, and emits src/generated/bundledLibsPayload.ts for runtime loading
 * (see src/utils/bundledLibs.ts).
 */
import esbuild from 'esbuild';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import LZString from 'lz-string';
import { sharedExternal } from '../esbuild.shared.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.join(__dirname, '..');
const scriptFileName = path.basename(__filename);

const result = await esbuild.build({
  absWorkingDir: root,
  entryPoints: ['src/bundled-libs-entry.ts'],
  bundle: true,
  platform: 'browser',
  format: 'iife',
  globalName: '__stewardBundledLibs',
  target: 'es2018',
  minify: true,
  write: false,
  external: sharedExternal,
  define: {
    'process.env.NODE_ENV': '"production"',
  },
  logLevel: 'info',
});

const js = result.outputFiles[0].text;
const b64 = LZString.compressToBase64(js);
const outDir = path.join(root, 'src/generated');
const outFile = path.join(outDir, 'bundledLibsPayload.ts');
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(
  outFile,
  `// AUTO-GENERATED by ${scriptFileName} — do not edit manually\n` +
    `export const BUNDLED_LIBS_LZ_B64 = ${JSON.stringify(b64)};\n`,
  'utf8'
);

console.log(
  `Wrote ${path.relative(root, outFile)} (base64 length ${b64.length}, minified JS ${js.length} bytes)`
);
