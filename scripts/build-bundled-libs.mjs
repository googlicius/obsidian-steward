/**
 * Bundles heavy npm dependencies (currently ai + @ai-sdk/* + ollama) into one IIFE,
 * LZ-compresses it, and emits src/generated/bundledLibsPayload.ts for runtime loading
 * (see src/utils/bundledLibs.ts).
 */
import esbuild from 'esbuild';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import LZString from 'lz-string';
import builtins from 'builtin-modules';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

const external = [
  'obsidian',
  'electron',
  '@codemirror/autocomplete',
  '@codemirror/collab',
  '@codemirror/commands',
  '@codemirror/language',
  '@codemirror/lint',
  '@codemirror/search',
  '@codemirror/state',
  '@codemirror/view',
  '@lezer/common',
  '@lezer/highlight',
  '@lezer/lr',
  ...builtins,
];

const result = await esbuild.build({
  absWorkingDir: root,
  entryPoints: ['src/bundled-libs-entry.ts'],
  bundle: true,
  platform: 'browser',
  format: 'iife',
  globalName: '__stewardBundledLibs',
  target: 'es2018',
  minify: true,
  write: false,
  external,
  define: {
    'process.env.NODE_ENV': '"production"',
  },
  logLevel: 'info',
});

const js = result.outputFiles[0].text;
const b64 = LZString.compressToBase64(js);
const outDir = path.join(root, 'src/generated');
const outFile = path.join(outDir, 'bundledLibsPayload.ts');
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(
  outFile,
  `// AUTO-GENERATED by scripts/build-bundled-libs.mjs — do not edit manually\n` +
    `export const BUNDLED_LIBS_LZ_B64 = ${JSON.stringify(b64)};\n`,
  'utf8'
);

console.log(
  `Wrote ${path.relative(root, outFile)} (base64 length ${b64.length}, minified JS ${js.length} bytes)`
);
